def fix_card(filename, config):
    path = os.path.join(BASE, filename)
    with open(path, "r", encoding="utf-8") as f:
        card = json.load(f)

    # 1. Set avatar
    card["avatar"] = config["avatar"]

    # 2. Inject system_prompt
    card["system_prompt"] = SYSTEM_PROMPT

    # 3. Expand first_mes
    card["first_mes"] = config["first_mes"]

    # 4. Expand scenario
    card["scenario"] = config["scenario"]

    # 5. Expand description (Lena only)
    if "description" in config:
        card["description"] = config["description"]

    # 6. Update data sub-object
    if "data" in card:
        card["data"]["avatar"] = config["avatar"]
        card["data"]["system_prompt"] = SYSTEM_PROMPT
        card["data"]["first_mes"] = config["first_mes"]
        card["data"]["scenario"] = config["scenario"]
        if "description" in config:
            card["data"]["description"] = config["description"]

    # Write back
    with open(path, "w", encoding="utf-8") as f:
        json.dump(card, f, ensure_ascii=False, indent=2)

    # Verify
    with open(path, "r", encoding="utf-8") as f:
        json.load(f)  # will throw if invalid

    print(f"✓ {filename}")


