modified = 0
for k, v in data['entries'].items():
    content = v.get('content', '')
    
    # 1. NEO entry
    if v.get('comment') == 'NEO' and '背景设定:' in content and '性格调色盘' not in content:
        # Insert before 背景設定:
        idx = content.find('\n\n背景设定:')
