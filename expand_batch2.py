"""
Expand remaining 10 Metaverse world book entries.
"""
import json
import re

JSON_PATH = r"C:\Users\nopsi\Desktop\metaverse工程\card\CHUNITHM-Metaverse.json"

with open(JSON_PATH, 'r', encoding='utf-8') as f:
    data = json.load(f)

reports = []

def count_chinese(text):
    return len(re.findall(r'[\u4e00-\u9fff]', text))

def update_entry(comment, new_content):
    for key, entry in data['entries'].items():
        if entry.get('comment', '') == comment:
            old_count = count_chinese(entry['content'])
            entry['content'] = new_content
            new_count = count_chinese(new_content)
            reports.append((comment, old_count, new_count))
            print(f"  {comment}: {old_count}字 → {new_count}字 (+{new_count - old_count})")
            return True
    print(f"ERROR: Entry '{comment}' not found!")
    return False

