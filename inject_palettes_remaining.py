def main():
    json_path = r"C:\Users\nopsi\Desktop\metaverse工程\new_card\Metaverse-New.json"
    
    with open(json_path, "r", encoding="utf-8") as f:
        data = json.load(f)
    
    updated = 0
    for uid, info in PALETTES.items():
        if uid not in data["entries"]:
            print(f"WARNING: uid={uid} ({info['name']}) not found in entries, skipping")
            continue
        
        entry = data["entries"][uid]
        content = entry.get("content", "")
        
        if "性格调色盘" in content:
            print(f"SKIP: uid={uid} ({info['name']}) already has palette")
            continue
        
        # Find the position to insert palette: after "背景设定:" or before "</npc>"
        palette_text = "\n" + info["palette"] + "\n"
        
        # Insert before 背景设定 or before </npc>
        if "背景设定:" in content:
            content = content.replace("背景设定:", palette_text + "背景设定:")
        elif "</npc>" in content:
            content = content.replace("</npc>", palette_text + "</npc>")
        else:
            print(f"WARNING: uid={uid} ({info['name']}) has no '背景设定:' or '</npc>' marker")
            continue
        
        entry["content"] = content
        updated += 1
        print(f"OK: uid={uid} ({info['name']}) palette injected")
    
    # Save
    with open(json_path, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
    
    print(f"\nTotal updated: {updated}/{len(PALETTES)}")
    return updated


